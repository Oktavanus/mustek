          <p>Dinas Pariwisata dan Kebudayaan Provinsi Daerah Khusus Ibukota Jakarta adalah salah satu dari dinas pemerintah provinsi DKI Jakarta. Dinas ini bertugas dan bertanggung jawab terhadap segala hal terkait kebudayaan dan kepariwisataan di wilayah Jakarta dan Kepulauan Seribu.</p>
          <h4>Sejarah</h4>
          <p>Sebelum Pemerintah Provinsi DKI Jakarta membentuk Dinas Pariwisata dan Kebudayaan, pemerintah DKI Jakarta telah membentuk Badan Pembimbing Pariwisata Daerah Khusus Ibukota Jakarta (Bapparda Djaya) pada tanggal 7 Februari 1967 sebagai realisasi dari Keputusan Presidium Kabinet Ampera Nomor 103/4TAP/2/66.</p>
          <p>Bapparda Djaya inilah yang menjadi cikal bakal Dinas Pariwisata DKI Jakarta. Walaupun secara administratif kehadiran Dinas Pariwisata DKI Jakarta tertulis tanggal 20 September 1970, namun tanggal 7 Februari 1967 telah ditetapkan sebagai hari jadi Dinas Pariwisata DKI Jakarta.</p>
          <p>Berkembangnya kepariwisataan di dunia sebagai sektor non migas yang menjadi andalan devisa negara, mendorong daerah-daerah lainnya di Indonesia berlomba-lomba mengembangkan potensi daerah yang dimilikinya. Pemerintah Provinsi DKI Jakarta pada awalnya membentuk Unit Organisasi yang khusus menangani kepariwisataan di Jakarta. Berdasarkan Perda No. 3 Tahun 2001 Dinas Pariwisata Provinsi DKI Jakarta dibentuk, sedangkan Struktur Organisasi dan Tata Kerja dibentuk berdasarkan Peraturan Gubernur Provinsi DKI Jakarta No. 107 Tahun 2001.</p>
          <p>Dengan terjadinya perubahan/reformasi birokrasi, baik di tingkat pusat maupun daerah, maka pada beberapa struktur organisasi pada Pemerintah Provinsi DKI Jakarta mengalami perubahan. Berdasarkan Peraturan Daerah Nomor 10 tahun 2008 tentang Organisasi Perangkat Daerah, Dinas Pariwisata dan Dinas Kebudayaan dan Permuseuman bergabung menjadi Dinas Pariwisata dan Kebudayaan, sedangkan Struktur Organisasi dan Tata Kerja diatur berdasarkan Peraturan Gubernur Nomor 107 Tahun 2009.</p>
          <h4>TUGAS POKOK & FUNGSI</h4>
          <p>• Penyusunan, dan pelaksanaan rencana kerja dan anggaran dinas pariwisata dan kebudayaan;<br>
          • Perumusan kebijakan teknis pelaksanaan urusan kepariwisataan dan kebudayaan;<br>
          • Penyelenggaraan urusan kepariwisataan dan kebudayaan;<br>
          • Pembinaan dan pengembangan industri pariwisata dan budaya;<br>
          • Pemberdayaan masyarakat kepariwisataan dan kebudayaan;<br>
          • Pengkajian dan pengembangan urusan kepariwisataan dan kebudayaan;<br>
          • Pengawasan, pengendalian dan penindakan di bidang urusan kepariwisataan dan kebudayaan;<br>
          • Pelayanan, pembinaan, dan pengendalian rekomendasi sertifikasi dan/atau perizinan usaha di bidang kepariwisataan dan kebudayaan;<br>
          • Pemungutan, penatausahaan, penyetoran, pelaporan, dan pertanggungjawaban penerimaan retribusi di bidang kepariwisataan dan kebudayaan;<br>
          • Pembinaan dan pengembangan tenaga fungsional dan tenaga teknis di bidang kepariwisataan dan kebudayaan;<br>
